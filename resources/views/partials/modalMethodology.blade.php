                            floods, fires and diseases. Biodiversity plays a role in stabilising the earth’s
                            climate; it contributes to sustainable livelihoods and economies; and
                            creates conditions that enable cultural diversity to thrive.
                        </p>
                        <p class="mb-4">
                            The maintenance of global biodiversity is particularly relevant for rural
                            communities in developing countries and for Indigenous Peoples, whose
                            livelihoods and survival may be highly dependent on the ecosystems
                            services supported through biodiversity, such as food, nutrients, medicines,
                            fuel, fibre, flood control, clean drinking water and sacred sites.
                        </p>
                        <p class="mb-4">
                            Mining companies, like other businesses and society as a whole, rely on
                            ecosystems and the services they provide. However, mining also has the
                            potential to directly affect biodiversity, for example through the clearing
                            of vegetation for roads, removal of primary forests and soils to access
                            ore bodies, the conversion of land, wetlands or water-bodies into waste
                            disposal sites, and planned or unplanned discharges of waste products
                            to the environment. There may also be indirect impacts on biodiversity
                            and ecosystem services from mining, such as increased pressures on
                            wildlife for trade or bushmeat when mining roads are built in previously
                            inaccessible areas, or intensified clearing of land as a result of the inmigration of mine workers or others seeking economic opportunities.
                        </p>
                        <p class="mb-4">
                            Growing awareness of the potential direct, indirect, induced and cumulative
                            impacts of mining on biodiversity and of the dependencies of companies on
                            biodiversity and ecosystem services is leading many mining companies to
                            carry out biodiversity assessments and develop systems and approaches
                            to avoid critical habitats and key biodiversity areas whenever possible.
                        </p>
                    </div>
                    <div class="sm:w-6/12 w-full">
                        <p class="mb-4">
                            Some mining companies are also applying the “mitigation hierarchy” as
                            a means of managing biodiversity risks. The mitigation hierarchy is an
                            internationally recognised framework that prioritises avoidance of impacts
                            on biodiversity and ecosystem services, and, if that is not possible, moves
                            to minimisation, restoration and, as a last resort, the offsetting of residual
                            impacts. While impact mitigation is an interactive process throughout the
                            project lifecycle, opportunities for impact avoidance are greatest at the
                            planning phase of development.
                        </p>
                        <p class="mb-4">
                            Offsetting is the last option in the hierarchy because it comes with a set
                            of risks, including uncertainty of success, economic and governance
                            challenges to sustaining offsets in perpetuity, and the potential for
                            proposed offset projects to be socially or culturally unacceptable to relevant
                            stakeholders. Where offsetting occurs, it should be carefully designed
                            and guided by principles such as replacement of impacted biodiversity
                            on an ecologically-equivalent, or like-for-like or better basis; no net loss
                            and preferably a net gain of biodiversity; consultation with stakeholders to
                            determine acceptable offsets; and creation of long-term mechanisms to
                            fund offset projects.
                        </p>
                        <p class="mb-4">
                            As with any responsible environmental management system, the
                            identification of risks, development of effective mitigation strategies
                            and monitoring plans include the involvement of relevant stakeholders.
                            Actions may also be designed or reviewed by experienced biologists and
                            other specialists to ensure that mitigation is optimised in accordance with
                            the hierarchy. Increasingly, companies are commissioning independent
                            external audits or oversight to verify whether their biodiversity management
                            strategies are being effectively implemented. Such external oversight is
                            a useful means of building stakeholder trust and confidence that mining
                            activities are not posing significant threats to biodiversity and important
                            ecosystem services.
                        </p>
                        <p class="mb-4">
                            The business case for responsible biodiversity management is strong.
                            Companies that take a proactive approach to biodiversity and ecosystem
                            services management may experience a competitive advantage as
                            regulatory regimes in areas with increasing pressures on biodiversity
                            shift to more protective policies. Those companies that demonstrate good
                            management practices, including application of the mitigation hierarchy
                            and external audits of their management practices, may secure easier
                            and less costly access to capital, land and resources. Strong approaches
                            to protecting biodiversity and ecosystem services help to build trust
                            with communities, non-governmental organisations, producing country
                            governments and other stakeholders, thus strengthening the company’s
                            social licence to operate.
                        </p>

